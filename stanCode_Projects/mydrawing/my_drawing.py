def set_up_triangle(x1, y1, x2, y2, x3, y3, color):
    triangle = GPolygon()
    triangle.add_vertex((x1, y1-50))
    triangle.add_vertex((x2, y2-50))
    triangle.add_vertex((x3, y3-50))
    triangle.filled = True
    triangle.color = color
    triangle.fill_color = color
    window.add(triangle)


def set_up_quadrilateral(x1, y1, x2, y2, x3, y3, x4, y4, color):
    quadrilateral = GPolygon()
    quadrilateral.add_vertex((x1, y1-50))
    quadrilateral.add_vertex((x2, y2-50))
    quadrilateral.add_vertex((x3, y3-50))
    quadrilateral.add_vertex((x4, y4-50))
    quadrilateral.filled = True
    quadrilateral.color = color
    quadrilateral.fill_color = color
    window.add(quadrilateral)


